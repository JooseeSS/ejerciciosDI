  private void inicializarTabla(){
  DefaultTableModel dtm = new DefaultTableModel();
  dtm.setColumnIdentifiers(new String[]{"Nombre Operacion", "1º numero", "2º numero", "Resultado"});   
  jTableOperaciones.setModel(dtm);
  sorter = new TableRowSorter<>(dtm);
    jTableOperaciones.setRowSorter(sorter);

    // 4. (CLAVE) DECIRLE CÓMO ORDENAR LA COLUMNA "RESULTADO" (índice 3)
    // Forzamos a que compare los Strings como si fueran números (Doubles)
    sorter.setComparator(3, new Comparator<String>() {
        @Override
        public int compare(String s1, String s2) {
            try {
                // Intenta convertir el texto a número para comparar
                Double d1 = Double.parseDouble(s1);
                Double d2 = Double.parseDouble(s2);
                return d1.compareTo(d2); // Comparación numérica
            } catch (NumberFormatException e) {
                // Si algo falla (ej. texto vacío), usa la comparación normal
                return s1.compareTo(s2);
            }
        }
    });

    // 5. ESTABLECER EL ORDEN POR DEFECTO: Col 3 (Resultado), Descendente
    List<RowSorter.SortKey> sortKeys = new ArrayList<>();
    sortKeys.add(new RowSorter.SortKey(3, SortOrder.DESCENDING));
    
    sorter.setSortKeys(sortKeys);
  }
  
