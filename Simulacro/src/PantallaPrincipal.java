    private void jButtonEliminarOperacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarOperacionActionPerformed
        // 1. OBTENER EL MODELO DE LA TABLA
    DefaultTableModel dtm = (DefaultTableModel) jTableOperaciones.getModel();

    // 2. PEDIR DATOS (Pop-up de búsqueda)
    // Para simplificar, buscaremos por "Nombre de la Operación".
    String nombreBuscado = JOptionPane.showInputDialog(this,"Introduce el Nombre de la Operación a eliminar:", "Buscar Operación",JOptionPane.QUESTION_MESSAGE);
           


    // Si el usuario presiona "Cancelar" o no escribe nada, salimos.
    if (nombreBuscado == null) {
        return; // No hacer nada
    }

    // --- Búsqueda de la Fila ---
    int filaEncontrada = -1; // -1 significa que no la hemos encontrado
    String nombreOperacion = "";
    String resultadoOperacion = ""; // Guardamos los datos para los mensajes

    // Recorremos toda la tabla
    for (int i = 0; i < dtm.getRowCount(); i++) {
        // Obtenemos el valor de la columna 0 (Nombre Operacion)
        String nombreEnTabla = (String) dtm.getValueAt(i, 0);
        
        // Comparamos si es el que buscamos (ignorando mayús/minús)
        if (nombreEnTabla.equalsIgnoreCase(nombreBuscado)) {
            filaEncontrada = i; // ¡La encontramos! Guardamos su índice
            
            // Guardamos los datos de esa fila para usarlos en los mensajes
            nombreOperacion = (String) dtm.getValueAt(i, 0);
            resultadoOperacion = (String) dtm.getValueAt(i, 3); // Columna 3 es "Resultado"
            
            break; // Dejamos de buscar
        }
    }

    // --- Verificación y Confirmación ---

    if (filaEncontrada == -1) {
        // CASO "NO ENCONTRADO"
     JOptionPane.showMessageDialog(this,
                "No se encontró ninguna operación con el nombre '" + nombreBuscado + "'.",
                "Error de Búsqueda",
              JOptionPane.WARNING_MESSAGE);
    } else {
        // CASO "ENCONTRADO"
        // 3. POP-UP DE CONFIRMACIÓN (SÍ/NO)
        int confirmacion =JOptionPane.showConfirmDialog(this,
                "¿Seguro que quieres eliminar la operación '" + nombreOperacion + "'?",
                "Confirmar Eliminación",
             JOptionPane.YES_NO_OPTION,
               JOptionPane.WARNING_MESSAGE);

        if (confirmacion == JOptionPane.YES_OPTION) {
            // ■ Si confirma SÍ →
           JOptionPane.showMessageDialog(this,
                    "De acuerdo, eliminamos la operación " + nombreOperacion + " con resultado " + resultadoOperacion,
                    "Operación Eliminada",
                    JOptionPane.INFORMATION_MESSAGE);
            
            // Finalmente, eliminamos la fila del modelo
            dtm.removeRow(filaEncontrada);
            
        } else {
            // ■ Si pulsa NO →
         JOptionPane.showMessageDialog(this,
                    "Sabía que no te querías perder la operación " + nombreOperacion + " con resultado " + resultadoOperacion,
                    "Operación Conservada",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButtonEliminarOperacionActionPerformed
  
